#include "libft.h"
#include <stdlib.h>

static char	*ft_strcpy(char	*dst,	char const	*src)
{
	int	i;

	i = 0;
	while (src[i])
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}

static char	*ft_strcat(char	*s1, char const *s2)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (s1[i])
		i++;
	while (s2[j])
	{
		s1[i] = s2[j];
		i++;
		j++;
	}
	s1[i] = '\0';
	return (s1);
}

char	*ft_strjoin(char const *s1, char const *s2)
{
	char	*ptr;

	if (!s1 || !s2)
		return (NULL);
	ptr = (char *) malloc((ft_strlen (s1) + ft_strlen (s2) + 1) * sizeof(char));
	if (!ptr)
		return (NULL);
	else
		ft_strcpy (ptr, s1);
	ft_strcat (ptr, s2);
	return (ptr);
}
