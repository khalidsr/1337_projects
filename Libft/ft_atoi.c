#include "libft.h"

int	ft_atoi(const char *str)
{
	size_t	res;
	int		signe;
	size_t	i;

	i = 0;
	signe = 1;
	res = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == ' ')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i++] == '-')
			signe = signe * (-1);
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		res = res * 10 + str[i++] - '0';
		if (res > 9223372036854775807)
		{
			if (signe == -1)
				return (0);
			return (-1);
		}
	}
	return (res * signe);
}
